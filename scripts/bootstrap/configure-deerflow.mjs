function main() {
  const args = parseArgs(process.argv);
  const vclawRoot = path.resolve(args["vclaw-root"] ?? process.cwd());
  const deerflowRoot = path.resolve(args["deerflow-root"] ?? path.join(vclawRoot, ".vclaw", "deerflow"));
  const pythonBin = resolveCommandOrPath(args["python-bin"] ?? "python");
  const backendPath = path.join(deerflowRoot, "backend");
  const configPath = path.join(deerflowRoot, "config.yaml");
  const runtimePath = path.join(deerflowRoot, "runtime.json");
  const marker = "# Generated by Vclaw bootstrap.";
  const provider = chooseProvider();
  const enabled = provider.configured;

  ensureDir(deerflowRoot);

  const shouldWriteConfig =
    !args["keep-config"] ||
    (() => {
      try {
        return readFileSync(configPath, "utf8").startsWith(marker);
      } catch {
        return true;
      }
    })();

  if (shouldWriteConfig) {
    writeFileSync(configPath, buildConfig(provider, deerflowRoot), "utf8");
  }

  const runtime = {
    enabled,
    status: enabled ? "ready" : "credentials-required",
    provider: provider.id,
    providerEnvKey: provider.envKey,
    repoRoot: deerflowRoot,
    backendPath,
    configPath,
    pythonBin,
    mode: args.mode ?? "ultra",
    managedBy: "vclaw-bootstrap",
    installedAt: new Date().toISOString(),
  };
  writeFileSync(runtimePath, JSON.stringify(runtime, null, 2), "utf8");
  process.stdout.write(`${JSON.stringify(runtime, null, 2)}\n`);
}

main();
